package com.team13.game;

public class Speed
{

    Speed()
    {

    }

    // horizontal
    private float HSpeed;
    // vertical
    private float VSpeed;

    // getters
    public float getHSpeed()
    {
        return HSpeed;
    }

    public float getVSpeed()
    {
        return VSpeed;
    }

    // setters
    public void setVSpeed(float v)
    {
        VSpeed = v;
    }

    public void setHSpeed(float h)
    {
        HSpeed = h;
    }


}
