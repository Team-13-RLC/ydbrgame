package com.team13.game;

public class Position
{

    private float posX;
    private float posY;

    Position()
    {

    }

    // getters

    public float getPosX()
    {
        return posX;
    }

    public float getPosY()
    {
        return posY;
    }

    // setters

    public void setPosX(float x)
    {
        posX = x;
    }

    public void setPosY(float y)
    {
        posY = y;
    }

}
